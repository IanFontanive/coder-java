package fundamentos;

public class Inferencia {
	public static void main(String[] args) {
		
		double a = 4.5; // declaracão e inicialização
		System.out.println(a);
		
		a = 12;
		System.out.println(a);
		
		var b = 4.5;
		System.out.println(b);
		
		/*
		 * O java consegue definir o tipo de variável sem
		 * ser declarada. Valores flutuantes recebem valores
		 * inteiros, mas o inverso não.
		 */
	}
}
